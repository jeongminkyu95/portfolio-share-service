import { Schema, model } from "mongoose";

const PostSchema = new Schema (
    {
        id: {
            type: String,
            required: true,
        },
        user_id: {
            type: String,
            required: true,
        },
        title: {
            type: String,
            required: true,
        },
        description: {
            type: String,
            required: true,
        },
    },
    {
        timestamps: true,
    }
);

const PostModel = model( "Post", PostSchema);

export { PostModel };